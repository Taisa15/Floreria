    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        Flores florSeleccionada = (Flores) cmbFlores.getSelectedItem();
        java.util.Date utilDate = jdcFecha.getDate();
        java.sql.Date fecha = new java.sql.Date(utilDate.getTime());
        int cantidadVendida = Integer.parseInt(txtCantidad.getText());
        double precio = florSeleccionada.getPrecio();
        //int idFlores = florSeleccionada.getId();
        double descuento = Double.parseDouble(txtDescuento.getText());

        if (CantidadDisponible(florSeleccionada, cantidadVendida)) {
            double precioTotal = precio * cantidadVendida;

            Venta venta = new Venta(0, fecha, cantidadVendida, precio, florSeleccionada, descuento, precioTotal);

            actualizarInventario(florSeleccionada, cantidadVendida);
            guardarVenta(venta);

            JOptionPane.showMessageDialog(this, "Venta registrada con Ã©xito.");
        } else {
            JOptionPane.showMessageDialog(this, "No hay suficiente cantidad disponible para realizar la venta.");
        }
        ReporteVentaList rv = new ReporteVentaList();
        rv.setVisible(true);
        hide();


    }//GEN-LAST:event_btnGuardarActionPerformed

