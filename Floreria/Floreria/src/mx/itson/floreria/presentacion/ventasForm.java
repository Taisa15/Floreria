    public void cargarFlores() {
        flores = FloresDAO.obtenerTodos();

        DefaultComboBoxModel<Flores> comboBoxModel = new DefaultComboBoxModel<>();
        for (Flores flor : flores) {
            comboBoxModel.addElement(flor);
        }

        cmbFlores.setModel(comboBoxModel);
        cmbFlores.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Flores florSeleccionada = (Flores) cmbFlores.getSelectedItem();
                txtPrecio.setText(String.valueOf(florSeleccionada.getPrecio()));
            }
        });
    }

