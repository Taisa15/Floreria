    private boolean CantidadDisponible(Flores flor, int cantidadVendida) {
        return flor.getCantidad() >= cantidadVendida;
    }

    private void actualizarInventario(Flores flor, int cantidadVendida) {
        int cantidadActualizada = flor.getCantidad() - cantidadVendida;
        flor.setCantidad(cantidadActualizada);
        FloresDAO.actualizar(flor);
    }

